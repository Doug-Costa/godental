  <h1 style="margin-top: 100px;">Política de Cancelamento – DentalGo</h1>
  
      <p>A presente política tem como objetivo estabelecer regras claras e transparentes para o <strong>cancelamento de assinaturas e serviços contratados na plataforma DentalGo</strong>, conforme previsto no Código Civil e no Código de Processo Civil.</p>
  
      <div class="section">
          <h2>1. Direito de Arrependimento (Art. 49 do CDC)</h2>
          <p>Nos casos em que o contratante for consumidor final (pessoa física) e a contratação tiver ocorrido fora do estabelecimento comercial, especialmente por meio eletrônico, é garantido o direito de arrependimento no prazo de <strong>7 (sete) dias corridos</strong> a contar da contratação ou do início da utilização do serviço, conforme o artigo 49 do Código de Defesa do Consumidor (aplicado subsidiariamente).</p>
          <ul>
              <li>• O cancelamento por arrependimento deverá ser solicitado por e-mail ou via área do assinante;</li>
              <li>• Haverá reembolso integral de eventuais valores pagos, no prazo de até 10 dias úteis.</li>
          </ul>
      </div>
  
      <div class="section">
          <h2>2. Cancelamento de Assinatura Mensal</h2>
          <p>A assinatura mensal pode ser cancelada a qualquer momento pelo assinante, com os seguintes efeitos:</p>
          <ul>
              <li>• O cancelamento terá efeito <strong>no ciclo seguinte</strong> ao da solicitação;</li>
              <li>• O acesso à plataforma será mantido até o final do período já pago;</li>
              <li>• Não haverá reembolso proporcional de valores pagos referentes ao mês vigente.</li>
          </ul>
      </div>
  
      <div class="section">
          <h2>3. Cancelamento de Assinatura Anual</h2>
          <p>O cancelamento da assinatura anual antecipadamente estará sujeito às seguintes condições:</p>
          <ul>
              <li>• Se o cancelamento for solicitado <strong>antes de 7 dias corridos</strong>, aplica-se o direito de arrependimento com reembolso integral;</li>
              <li>• <strong>Após o 8º dia</strong>, o contratante poderá optar por:
                  <ul>
                      <li>• <strong>Cancelamento sem devolução de valores</strong>, mantendo o acesso até o fim do ciclo;</li>
                      <li>• Ou solicitar cancelamento com devolução proporcional, mediante retenção de <strong>multa compensatória de 10% do valor restante</strong> do contrato (art. 413 do Código Civil), respeitando o princípio da função social do contrato (art. 421).</li>
                  </ul>
              </li>
          </ul>
      </div>
  
      <div class="section">
          <h2>4. Cancelamento por Inadimplemento</h2>
          <p>O não pagamento de quaisquer parcelas autoriza a rescisão contratual por inadimplemento, nos termos do <strong>art. 475 do Código Civil</strong>, podendo haver:</p>
          <ul>
              <li>• Suspensão imediata do acesso à plataforma;</li>
              <li>• Cobrança proporcional dos valores vencidos até a data do cancelamento;</li>
              <li>• Eventual inclusão em cadastro de inadimplentes, conforme legislação vigente.</li>
          </ul>
      </div>
  
      <div class="section">
          <h2>5. Reajustes e Condições de Renovação</h2>
          <ul>
              <li>• A assinatura será renovada automaticamente, salvo solicitação contrária do contratante até 5 dias antes do vencimento;</li>
              <li>• Os valores poderão ser reajustados anualmente conforme o índice <strong>IPCA</strong> ou outro legalmente permitido, com aviso prévio de 30 dias.</li>
          </ul>
      </div>
  
